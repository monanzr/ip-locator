export const validateIpv4 = (ip) => {
    const ipv4Regex = /^(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/;
    return ipv4Regex.test(ip);
  };
  
  export const validateIpv6 = (ip) => {
    const ipv6Regex = /((([0-9A-Fa-f]{1,4}:){7}([0-9A-Fa-f]{1,4}|:))|(([0-9A-Fa-f]{1,4}:){1,7}:)|(([0-9A-Fa-f]{1,4}:){1,6}(:[0-9A-Fa-f]{1,4}|:)){2}|(([0-9A-Fa-f]{1,4}:){1,5}(:[0-9A-Fa-f]{1,4}|:)){3}|(([0-9A-Fa-f]{1,4}:){1,4}(:[0-9A-Fa-f]{1,4}|:)){4}|(([0-9A-Fa-f]{1,4}:){1,3}(:[0-9A-Fa-f]{1,4}|:)){5}|(([0-9A-Fa-f]{1,4}:){1,2}(:[0-9A-Fa-f]{1,4}|:)){6}|([0-9A-Fa-f]{1,4}:)((:[0-9A-Fa-f]{1,4}){1,7}|:)|:((:[0-9A-Fa-f]{1,4}){1,7}|:))$/;
    return ipv6Regex.test(ip);
  };

  export const onKeyNumber = (event) => {
    if (!/^[\d.]$/.test(event.key)) {
      event.preventDefault();
    }
  };
  